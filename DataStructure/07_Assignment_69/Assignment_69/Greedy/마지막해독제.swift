func 마지막해독제(arr: [Int]) -> Int {
  // 제조 시간이 짧은 순서대로 정렬
  let sortedTimes = arr.sorted()

  var totalTime = 0
  var currentWaitTime = 0

  for time in sortedTimes {
    // 현재 사람의 대기 시간 + 제조 시간
    currentWaitTime += time
    // 총 시간에 더함
    totalTime += currentWaitTime
  }

  return totalTime
}
